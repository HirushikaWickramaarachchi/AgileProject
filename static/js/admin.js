(() => {
    const toastElement = document.getElementById("adminActionToast");
    const toastMessage = document.getElementById("adminToastMessage");
    const toast = toastElement ? new bootstrap.Toast(toastElement) : null;

    const showToast = (message) => {
        if (!toast || !toastMessage) {
            return;
        }
        toastMessage.textContent = message;
        toast.show();
    };

    const clockElement = document.querySelector("[data-admin-clock]");
    if (clockElement) {
        const renderClock = () => {
            const now = new Date();
            clockElement.textContent = now.toLocaleString(undefined, {
                weekday: "short",
                hour: "2-digit",
                minute: "2-digit"
            });
        };
        renderClock();
        setInterval(renderClock, 60000);
    }

    document.querySelectorAll(".js-demo-action").forEach((button) => {
        button.addEventListener("click", () => {
            const actionName = button.dataset.actionName || "Action completed successfully";
            showToast(actionName);
        });
    });

    const eventForm = document.getElementById("eventForm");
    if (eventForm) {
        const eventId = document.getElementById("eventId");
        const titleInput = document.getElementById("eventTitle");
        const slugInput = document.getElementById("eventSlug");
        const clubInput = document.getElementById("eventClub");
        const categoryInput = document.getElementById("eventCategory");
        const startInput = document.getElementById("eventStart");
        const endInput = document.getElementById("eventEnd");
        const venueInput = document.getElementById("eventVenue");
        const descriptionInput = document.getElementById("eventDescription");
        const clearButton = document.getElementById("clearEventForm");

        const previewTitle = document.getElementById("previewTitle");
        const previewMeta = document.getElementById("previewMeta");
        const previewCategory = document.getElementById("previewCategory");
        const previewDescription = document.getElementById("previewDescription");

        const slugify = (value) =>
            value
                .toLowerCase()
                .trim()
                .replace(/[^a-z0-9\s-]/g, "")
                .replace(/\s+/g, "-")
                .replace(/-+/g, "-");

        const formatDate = (value) => {
            if (!value) {
                return "";
            }
            return new Date(value).toLocaleString(undefined, {
                month: "short",
                day: "numeric",
                hour: "2-digit",
                minute: "2-digit"
            });
        };

        const refreshPreview = () => {
            if (!previewTitle || !previewMeta || !previewCategory || !previewDescription) {
                return;
            }
            previewTitle.textContent = titleInput ? (titleInput.value || "Your event title") : "Your event title";
            const clubText = clubInput ? (clubInput.options[clubInput.selectedIndex]?.text || "") : "";
            previewCategory.textContent = clubText || "Category";
            previewDescription.textContent = descriptionInput ? (descriptionInput.value || "Your event description will appear here as you type.") : "";

            const dateLabel = (startInput && startInput.value) ? formatDate(startInput.value)
                : ((document.getElementById("eventDate") && document.getElementById("eventDate").value) ? document.getElementById("eventDate").value : "");
            const metaParts = [dateLabel, clubText].filter(Boolean);
            previewMeta.textContent = metaParts.length ? metaParts.join(" | ") : "Select date, venue, and club to see preview";
        };

        const validateTimeWindow = () => {
            if (!startInput || !endInput || !startInput.value || !endInput.value) {
                if (endInput) endInput.classList.remove("is-invalid");
                return true;
            }
            const valid = new Date(endInput.value) > new Date(startInput.value);
            endInput.classList.toggle("is-invalid", !valid);
            return valid;
        };

        titleInput.addEventListener("input", () => {
            if (slugInput && (!slugInput.value || slugInput.dataset.autogenerated === "true")) {
                slugInput.value = slugify(titleInput.value);
                slugInput.dataset.autogenerated = "true";
            }
            refreshPreview();
        });

        slugInput?.addEventListener("input", () => {
            slugInput.dataset.autogenerated = "false";
        });

        [clubInput, categoryInput, startInput, endInput, venueInput, descriptionInput,
         document.getElementById("eventDate")].forEach((input) => {
            if (!input) return;
            input.addEventListener("input", refreshPreview);
            input.addEventListener("change", refreshPreview);
        });

        [startInput, endInput].forEach((input) => {
            if (!input) return;
            input.addEventListener("change", validateTimeWindow);
        });

        document.querySelectorAll("[data-edit-event]").forEach((button) => {
            button.addEventListener("click", () => {
                eventId.value = button.dataset.eventId || "existing";
                titleInput.value = button.dataset.title || "";
                clubInput.value = button.dataset.club || "";
                categoryInput.value = button.dataset.category || "";
                startInput.value = button.dataset.start || "";
                endInput.value = button.dataset.end || "";
                venueInput.value = button.dataset.venue || "";
                document.getElementById("eventCapacity").value = button.dataset.capacity || "100";
                document.getElementById("eventStatus").value = button.dataset.status || "Draft";
                descriptionInput.value = button.dataset.description || "";
                slugInput.value = slugify(titleInput.value);
                slugInput.dataset.autogenerated = "true";

                refreshPreview();
                validateTimeWindow();
                showToast(`Loaded "${titleInput.value}" for editing`);
                window.scrollTo({ top: 0, behavior: "smooth" });
            });
        });

        clearButton?.addEventListener("click", () => {
            eventForm.reset();
            eventId.value = "new";
            slugInput.value = "";
            slugInput.dataset.autogenerated = "true";
            endInput.classList.remove("is-invalid");
            refreshPreview();
            showToast("Form reset. Ready for a new event.");
        });

        eventForm.addEventListener("submit", (event) => {
            const timeWindowValid = validateTimeWindow();
            if (!eventForm.checkValidity() || !timeWindowValid) {
                event.preventDefault();
                event.stopPropagation();
                eventForm.classList.add("was-validated");
                showToast("Please fix validation errors before publishing.");
                return;
            }

            eventForm.classList.remove("was-validated");
        });

        refreshPreview();
    }

    /* ── Event search/filter ────────────────────────────────── */
    const eventSearch = document.getElementById("eventSearch");
    const eventStatusFilter = document.getElementById("eventStatusFilter");
    const eventResetFilters = document.getElementById("eventResetFilters");
    const eventRows = Array.from(document.querySelectorAll("#eventsTableBody tr"));

    if (eventSearch || eventStatusFilter) {
        const applyEventFilters = () => {
            const query = (eventSearch?.value || "").toLowerCase().trim();
            const status = eventStatusFilter?.value || "all";

            eventRows.forEach((row) => {
                const text = row.textContent.toLowerCase();
                const statusBadge = row.querySelector(".badge")?.textContent.toLowerCase() || "";
                const matchesQuery = !query || text.includes(query);
                const matchesStatus = status === "all" || statusBadge.includes(status.toLowerCase());
                row.classList.toggle("d-none", !(matchesQuery && matchesStatus));
            });
        };

        eventSearch?.addEventListener("input", applyEventFilters);
        eventStatusFilter?.addEventListener("change", applyEventFilters);

        eventResetFilters?.addEventListener("click", () => {
            if (eventSearch) eventSearch.value = "";
            if (eventStatusFilter) eventStatusFilter.value = "all";
            applyEventFilters();
            showToast("Event filters cleared");
        });
    }

    /* ── Event delete ───────────────────────────────────────── */
    document.querySelectorAll(".js-delete-event").forEach((button) => {
        button.addEventListener("click", () => {
            const row = button.closest("tr");
            const title = button.dataset.eventTitle || "this event";
            if (confirm(`Delete "${title}"? This cannot be undone.`)) {
                row.remove();
                showToast(`"${title}" deleted`);
            }
        });
    });

    const membersTable = document.getElementById("membersTable");
    if (membersTable) {
        const searchInput = document.getElementById("memberSearch");
        const roleFilter = document.getElementById("memberRoleFilter");
        const statusFilter = document.getElementById("memberStatusFilter");
        const resetButton = document.getElementById("memberResetFilters");
        const rows = Array.from(document.querySelectorAll("[data-member-row]"));

        const applyFilters = () => {
            const query = (searchInput?.value || "").toLowerCase().trim();
            const role = roleFilter?.value || "all";
            const status = statusFilter?.value || "all";

            rows.forEach((row) => {
                const matchesQuery = !query || row.dataset.search.includes(query);
                const matchesRole = role === "all" || row.dataset.role === role;
                const matchesStatus = status === "all" || row.dataset.status === status;
                row.classList.toggle("d-none", !(matchesQuery && matchesRole && matchesStatus));
            });
        };

        [searchInput, roleFilter, statusFilter].forEach((control) => {
            control?.addEventListener("input", applyFilters);
            control?.addEventListener("change", applyFilters);
        });

        resetButton?.addEventListener("click", () => {
            if (searchInput) {
                searchInput.value = "";
            }
            if (roleFilter) {
                roleFilter.value = "all";
            }
            if (statusFilter) {
                statusFilter.value = "all";
            }
            applyFilters();
            showToast("Member filters cleared");
        });

        membersTable.querySelectorAll(".js-role-change").forEach((button) => {
            button.addEventListener("click", () => {
                const row = button.closest("[data-member-row]");
                if (!row) {
                    return;
                }

                const targetRole = button.dataset.targetRowRole;
                const roleBadge = row.querySelector(".member-role-badge");
                if (!targetRole || !roleBadge) {
                    return;
                }

                row.dataset.role = targetRole;
                const labelMap = {
                    admin: { text: "Admin", className: "text-bg-primary" },
                    moderator: { text: "Moderator", className: "text-bg-info" },
                    member: { text: "Member", className: "text-bg-secondary" }
                };

                const roleData = labelMap[targetRole];
                roleBadge.className = `badge ${roleData.className} member-role-badge`;
                roleBadge.textContent = roleData.text;
                applyFilters();
                showToast(`Role updated to ${roleData.text}`);
            });
        });
    }
    /* ── Remove member from club ────────────────────────────── */
    document.querySelectorAll(".js-remove-member").forEach((button) => {
        button.addEventListener("click", () => {
            const row = button.closest("[data-member-row]");
            const name = row?.querySelector(".fw-semibold.small")?.textContent || "this member";
            if (confirm(`Remove "${name}" from their club? This cannot be undone.`)) {
                row.remove();
                showToast(`"${name}" removed from club`);
            }
        });
    });

    /* ── Dark mode ──────────────────────────────────────────── */
    const DARK_KEY      = 'adminDarkMode';
    const USER_DARK_KEY = 'darkMode';
    const root          = document.documentElement;
    const toggle        = document.getElementById('darkModeToggle');

    const applyDark = (dark) => {
        root.classList.toggle('admin-dark', dark);
        if (toggle) {
            const icon = toggle.querySelector('i');
            if (dark) {
                icon.className = 'bi bi-sun-fill';
                toggle.title   = 'Switch to light mode';
            } else {
                icon.className = 'bi bi-moon-stars-fill';
                toggle.title   = 'Switch to dark mode';
            }
        }
    };

    // Read either key so user-side dark mode carries over to admin
    const initDark = localStorage.getItem(DARK_KEY) === 'true' || localStorage.getItem(USER_DARK_KEY) === 'true';
    applyDark(initDark);

    if (toggle) {
        toggle.addEventListener('click', () => {
            const isDark = root.classList.toggle('admin-dark');
            // Write both keys so user pages stay in sync
            localStorage.setItem(DARK_KEY,      isDark);
            localStorage.setItem(USER_DARK_KEY, isDark);
            applyDark(isDark);
        });
    }
})();
